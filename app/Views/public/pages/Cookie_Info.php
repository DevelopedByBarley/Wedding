            <p><strong class="important pr-font-400">Mik a cookie-k?</strong></p>
            <p class="pr-font-200">A cookie, vagyis süti egy olyan fájl, (egy adatsor,) amit a weboldal látogatójának számítógépén,
                mobiltelefonján vagy egyéb, internetes hozzáférést biztosító eszközén hozhat létre a weboldalt
                megjelenítő program. A cookie-t a webszerver küldi a látogató böngészőjének, majd a böngésző
                visszaküldi a szervernek. A sütik kizárólag adatokat tartalmaznak, vagyis nem tartalmaznak
                futtatható állományokat, vírusokat, illetve kémprogramokat (spyware) és nem férnek hozzá a
                felhasználó merevlemezének adataihoz.<br>
                (A süti egy kódolt adatsor, egy elérési útvonal, ami csak az adott szerverrel együtt
                értelmezhető. Magát a felhasználó adatait a szerver tárolja, nem a süti. (Minden weboldal
                használ sütiket.))</p>
            <p><strong class="pr-font-400 important">Mire használhatóak a cookie-k?</strong></p>
            <p class="pr-font-200">Ezek a fájlok lehetővé teszik a felhasználó internetezésre használt eszközének felismerését, így
                releváns, a felhasználó igényeihez igazított tartalom megjelenítését. Ilyenek például a
                kizárólag felhasználói névvel és jelszóval elérhető oldalak, amik sütik nélkül nem működnének. A
                sütik a felhasználó számára egyszerűbb böngészést biztosítanak, és elősegítik, hogy a
                Tárhelypark minél kényelmesebb szolgáltatást nyújtson. Így például az oldalaink – anonim –
                látogatói statisztikáinak elkészítéséhez használt cookie-k, amelyek értékes információkkal
                szolgálnak az Tárhelyparknak a felhasználók oldalhasználati szokásaival kapcsolatosan. A kapott
                statisztikai eredményeket felhasználva tudjuk az oldal szerkezetét és tartalmát az igényeknek
                megfelelően alakítani, mindezt a felhasználók anonimitásának megőrzése mellett.</p>
            <p><strong class="important pr-font-400">Milyen cookie-kat használunk mi?</strong></p>
            <p class="pr-font-200">Az oldal kétféle sütit használhat: időszakosat és állandót. Az időszakos sütik olyan ideiglenes
                fájlok, amelyek a bejelentkezett időszak végéig, vagy az alkalmazás (böngésző) bezárásáig
                maradnak a felhasználó eszközén. Az állandó cookie-k pedig a paraméterükben meghatározott ideig,
                vagy manuális törlésükig vannak jelen a felhasználó eszközén.<br>
                Mire használjuk a cookie-kat?</p>
            <p><strong class="important pr-font-400">Egy látogatás során a weboldal a következő célból helyezhet el cookie-kat a felhasználó
                    internetezésre használt eszközén:</strong></p>
            <p class="pr-font-200">Bejelentkezés tényének rögzítése<br>
                Weboldal teljesítménymérése<br>
                Böngészés elemzése<br>
                Releváns reklámok megjelenítése</p>
            <p><strong class="important pr-font-400">Tartalmaznak a cookie-k személyes adatokat?</strong></p>
            <p class="pr-font-200">A legtöbb süti hatékony működéséhez semmilyen személyes jellegű adat nem szükséges. Ennek
                megfelelően az esetek döntő többségében a cookie nem azonosítja az adott felhasználót. Ha egyes
                cookie-k használata során mégis gyűjtünk személyes adatokat, azokat kizárólag olyan célból
                használjuk fel, hogy bizonyos funkciók használatát megkönnyítsük a felhasználó számára. Ilyen
                például, hogy a böngésződ számára megjegyezhetővé tesszük a bejelentkezési adatokat vagy
                megőrizhetővé tesszük az oldalunkon a kosaradba tett termékeket. Ezek az adatok olyan módon
                vannak tárolva, hogy azokhoz jogosulatlan személyek nem férhetnek hozzá, illetve a cookie maga
                nem tartalmaz személyes adatot, csak a szerveren tárolt adatokhoz való elérési kulcsot, de ezt
                is kódolt formában.</p>
            <p><strong class="important pr-font-400">A cookie-k törlése</strong></p>
            <p class="pr-font-200">A weboldalak eléréséhez használt böngészőprogramok alapértelmezett esetben engedélyezik a sütik
                elhelyezését az eszközre. A sütik kezelésére vonatkozó információk a böngésző beállításai között
                találhatóak. Ezek a beállítások megváltoztathatók úgy, hogy a cookie-k automatikus kezelését a
                böngésző blokkolja, vagy minden esetben értesítést küldjön a felhasználónak, ha az eszközre
                cookie-t küldtek. Fontos tudni, hogy a cookie-k használatának tiltása/korlátozása negatívan
                befolyásolhatja az oldal bizonyos funkcióinak működését, például a megrendelést vagy
                bejelentkezést.<br>
                Miért fontosak a cookie-k az interneten?</p>
            <p class="pr-font-200">Napjainkban az internet hatákony működéséhez elengedhetetlenek a sütik. Hozzásegítik a
                felhasználót a kényelmes böngészéshez az általuk megismert igények és érdeklődési körök révén,
                illetve olyan adatokat tárolnak, amelyek nélkül a weboldalak egyes funkciói működésképtelenek. A
                népszerű közösségi oldalak és az online szolgáltatások egyszerűen működésképtelenek a sütik
                használata nélkül.</p>
            <p class="pr-font-200">Jó tudni, hogy a cookie-k korlátozása, vagy tiltása nem jelenti azt, hogy többé nem kapsz online
                reklámokat. Mindössze annyit érhető el vele, hogy a megjelenő reklámok nem képesek figyelembe
                venni a böngészési szokásaidat, így nem igazodnak az igényeidhez és az érdeklődési
                köreidhez.</p>
            <p><strong class="important pr-font-400">Biztonsággal és adatbiztonsággal kapcsolatos tényezők</strong></p>
            <p class="pr-font-200">A cookie-k NEM vírusok! A cookie-k egyszerű szöveget tárolnak, nem kódrészletekből állnak, így
                nem futtathatók, ezért nem tekinthetőek programoknak, így vírusoknak sem.</p>
            <p class="pr-font-200">Fontos azonban kiemelni, hogy a sütik használhatók negatív célokra is, mivel a felhasználó
                igényeire és navigálási előzményeire vonatkozó információkat tárolhatnak. A cookie-kon keresztül
                folyamatosan áramlik az információ a weboldal és a böngésző között mindkét irányba, így ha egy
                támadó (hacker) beavatkozik az adattovábbításba, úgy a süti által tárolt információk
                lekövethetők.<br>
                A korszerű böngészők mindegyike alkalmas arra, hogy személyre szabhassa a böngészője
                beállításait a cookie-kra vonatkozóan, úgy, hogy a sütik használata mellett az Ön számára még
                kényelmes internetezést biztosítva is biztonságos legyen. Ha a számítógépét csak saját maga
                használja, akkor hosszabb lejárati időt is beállíthat a böngészési előzmények rögzítésére,
                illetve a személyes adatok tárolására vonatkozóan.</p>
            <p class="pr-font-200">Amennyiben a számítógépet másokkal megosztva használja, (például egy internet kávézóban), akkor
                érdemes a böngésző által megjegyzett adatokat minden egyes használatot követően törölni, vagy
                “inkognitó” ablakot használni a böngészéshez. Így lehetősége van arra, hogy biztonságos keretek
                között látogasson meg olyan oldalakat, melyek cookie-kat helyeznek el a gépen.</p>
            <p class="pr-font-200">Rendszeresen frissítse a kémprogramok elleni programokat és víruskeresőket! Sok esetben a
                kémprogramok ellen védő szoftverek valós időben figyelik a böngészést és figyelmeztetik,
                amennyiben nem biztonságos oldalt kíván betölteni a felhasználó.<br>
                A támadások jelentős hányada a böngésző biztonsági résein keresztül történik. Minél régebbi
                verziójú egy böngésző, annál több ilyen gyenge pont található meg benne. Célszerű mindig
                telepíteni a böngésző legfrissebb verzióját, vagy a feltelepített verziót frissíteni!</p>
